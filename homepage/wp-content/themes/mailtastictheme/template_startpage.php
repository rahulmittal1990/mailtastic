/**
* Wrapper for changing banner which is shown in the middle
* stops timer

 * @param {type} index
 * @returns {undefined} */
 
var currentVoiceIndex = 0;
var VoiceSliderTime = 5000;
var VoiceSliderTimer = setInterval(function(){ 

    var toShow = currentVoiceIndex +1;
    if(toShow === voicesStructure.length){
        toShow = 0;

    }
    currentVoiceIndex = toShow;
    changeVoiceInner(toShow);

}, VoiceSliderTime);


function changeVoice(index){
    clearInterval(VoiceSliderTimer);
    //stop timer
    changeVoiceInner(index);
    
}


/**
* Change banner index 

 * @param {type} index
 * @returns {undefined} */
function changeVoiceInner(index){
    currentIndex = index;
   
    var mainImage = jQuery("#voicebigimage");
            
    mainImage.fadeOut('fast', function () {
        mainImage.attr('src', voicesStructure[index].image);
        mainImage.fadeIn('fast');
    });
    jQuery("#voicename").text(voicesStructure[index].name);
    jQuery("#voicetext").html(voicesStructure[index].text);
    jQuery("#voiceposition").text(voicesStructure[index].position);
    jQuery("#voicecompanylogo").attr('src', voicesStructure[index].companylogo);
    jQuery("#voicecompanylogoLink").attr('href', voicesStructure[index].link);
    
    //set all navigation image to black/white
    for(var i = 0 ; i < voicesStructure.length; i++){
        jQuery("#voicesliderernavelementimg_" + i).attr('src', voicesStructure[i].imageBW);
    }
    //set active navigation image to color
    jQuery("#voicesliderernavelementimg_" + index).attr('src', voicesStructure[index].image );
    
    
    
    
}


    
    
