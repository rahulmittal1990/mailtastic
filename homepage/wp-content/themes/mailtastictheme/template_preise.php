/**
* Wrapper for changing banner which is shown in the middle
* stops timer

 * @param {type} index
 * @returns {undefined} */
 
var currentVoiceIndex = 0;
var VoiceSliderTime = 5000;
var VoiceSliderTimer = setInterval(function(){ 

    var toShow = currentVoiceIndex +1;
    if(toShow === voicesStructure.length){
        toShow = 0;

    }
    currentVoiceIndex = toShow;
    changeVoiceInner(toShow);

}, VoiceSliderTime);


function changeVoice(index){
    clearInterval(VoiceSliderTimer);
    //stop timer
    changeVoiceInner(index);
    
}


/**
* Change banner index 

 * @param {type} index
 * @returns {undefined} */
function changeVoiceInner(index){
    currentIndex = index;
   
    var mainImage = jQuery("#voicebigimage");
            
